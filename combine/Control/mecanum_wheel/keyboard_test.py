import keyboard

while True:
    if keyboard.is_pressed('w'):
        print('w')
    if keyboard.is_pressed('a'):
        print('a')
    if keyboard.is_pressed('s'):
        print('s')
    if keyboard.is_pressed('d'):
        print('d')
    if keyboard.is_pressed('LEFT'):
        print('LEFT')
    if keyboard.is_pressed('1'):
        print('1')